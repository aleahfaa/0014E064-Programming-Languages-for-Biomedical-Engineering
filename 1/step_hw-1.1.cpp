#include <iostream>
using namespace std;

int main() {
    // declare and initialize variables
    float f = 2.936;
    int i = 5;
    // perform multiplication and division
    float multiplication = f * i;
    float division = f / i;
    // print the results
    cout << "Multiplication Result (f * i) = " << multiplication << endl;
    cout << "Division Result (f / i) = " << division << endl;
    return 0;
}